require.register("component-type/index.js", function(exports, require, module){

/**
 * toString ref.
 */

var toString = Object.prototype.toString;

/**
 * Return the type of `val`.
 *
 * @param {Mixed} val
 * @return {String}
 * @api public
 */

module.exports = function(val){
  switch (toString.call(val)) {
    case '[object Function]': return 'function';
    case '[object Date]': return 'date';
    case '[object RegExp]': return 'regexp';
    case '[object Arguments]': return 'arguments';
    case '[object Array]': return 'array';
    case '[object String]': return 'string';
  }

  if (val === null) return 'null';
  if (val === undefined) return 'undefined';
  if (val && val.nodeType === 1) return 'element';
  if (val === Object(val)) return 'object';

  return typeof val;
};

});
require.register("component-each/index.js", function(exports, require, module){

/**
 * Module dependencies.
 */

var type = require('type');

/**
 * HOP reference.
 */

var has = Object.prototype.hasOwnProperty;

/**
 * Iterate the given `obj` and invoke `fn(val, i)`.
 *
 * @param {String|Array|Object} obj
 * @param {Function} fn
 * @api public
 */

module.exports = function(obj, fn){
  switch (type(obj)) {
    case 'array':
      return array(obj, fn);
    case 'object':
      if ('number' == typeof obj.length) return array(obj, fn);
      return object(obj, fn);
    case 'string':
      return string(obj, fn);
  }
};

/**
 * Iterate string chars.
 *
 * @param {String} obj
 * @param {Function} fn
 * @api private
 */

function string(obj, fn) {
  for (var i = 0; i < obj.length; ++i) {
    fn(obj.charAt(i), i);
  }
}

/**
 * Iterate object keys.
 *
 * @param {Object} obj
 * @param {Function} fn
 * @api private
 */

function object(obj, fn) {
  for (var key in obj) {
    if (has.call(obj, key)) {
      fn(key, obj[key]);
    }
  }
}

/**
 * Iterate array-ish.
 *
 * @param {Array|Object} obj
 * @param {Function} fn
 * @api private
 */

function array(obj, fn) {
  for (var i = 0; i < obj.length; ++i) {
    fn(obj[i], i);
  }
}
});
require.register("CamShaft-filesaver/index.js", function(exports, require, module){
/**
 * Module dependencies
 */
var emitter = require("emitter")
  , each = require("each");

/**
 * Defines
 */
var INIT = 0
  , WRITING = 1
  , DONE = 2
  , SAVEABLE_TYPE = "application/octet-stream"
  , EVENTS = require("./lib/events");

/**
 * Current engine for the filesystem
 */
var engine;

/**
 * Show save as modal for a blob
 *
 * @param {Blob} blob
 * @param {String} name
 */
module.exports = exports = function(blob, name) {
  // Defaults
  if(!name) name = "download";

  return new FileSaver(blob, name);
};

/**
 * Set the engine for the current browser
 */
exports.engine = function(fn) {
  engine = fn;
};

function FileSaver (blob, name) {
  if(!engine) throw new Error("No file engine found.\nUse `FileSaver#engine` to enable one");

  var self = this;

  // Bind the events
  each(EVENTS, function(event) {
    var onfn = "on"+event;
    self.on(event, function() {
      if(self[onfn]) self[onfn].apply(self, arguments);
    });
  });

  // Start the initial state
  self.readyState = INIT;

  // Handle the deletion queue
  self.deletionQueue = [];

  // Set the appropriate states
  self.on("writestart", function() {
    self.readyState = WRITING;
  });
  self.on("writeend", function() {
    self.readyState = DONE;
  });

  this.implementation = engine.call(self, blob, name);
};
var proto = FileSaver.prototype;

/**
 * Inherits from emitter
 */
emitter(proto);

proto.emitAll = function () {
  var self = this;
  each(["writestart", "progress", "write", "writeend"], function (event) {
    self.emit(event);
  })
}

/**
 * Aborts saving the blob
 */
proto.abort = function() {
  this.readyState = DONE;
  if(this.implementation.abort) this.implementation.abort();
  this.emit("abort");
};

});
